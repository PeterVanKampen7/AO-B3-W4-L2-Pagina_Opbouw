    <h1>The Lord of the Rings</h1>
    <section id="left">
        <p>
            The all time classic of the fantasy genre, written by the godfather of fantasy JRR Tolkien. I cant properly describe
            my love for this trilogy and all its other books in the series. This is one of the very few books/series that I have read
            more than once, in fact I have read the Lord of the Rings trilogy four times.
            <br><br>
            The story of the Lord of the Rings is relatively well known, but nonetheless, I shall endeavor to give a synopsys here. The Lord of 
            the Rings is the continuation of the story told in the book The Hobbit, but can easily be read without having read the Hobbit. It starts
            with a hobbit by the name of Bilbo Baggins, who owns a magic ring. When he gets older and wants to travel again he leaves the ring to
            his nephew Frodo Baggins.
            <br><br>
            Together with the wizard Gandalf the Grey, Frodo finds out that the ring is evil and belongs to the enemy, and that the enemy has found 
            out the locatiom of the ring. So while Gandalf leaves to go seek help from another wizard, Frodo and his friend Sam set out to travel
            to the Elven city of Imladris, called Rivendell in the human tongue. Here they believe the ring will be safer while they figure out what to
            do with it.
            <br><br>
            When they eventually make it to Rivendell, there is a council in which the races of men, elves, dwarves, hobbits and wizards are present. Here
            it is revealed that the ring must be destroyed and that it can only be done in a volcano deep in enemy territory, Mount Doom.
            <br><br>
            From the council a group of nine people are chosen to undertake this burden. Two men are chosen; Aragorn son of Arathorn, and Boromir
            of Gondor. One elf by the name of Legolas Greenleaf, one dwarf called Gimli son of Gloin, two other hobbits called Merry and Pippin, and Gandalf,
            Sam and Frodo, with Frodo taking the task of being ringbearer.
            <br><br>
            While they travel to Mount Doom they encounter many obstacles and hurdles, eventually splintering and continueing their quest in seperate 
            groups. Their travel takes them through dark caves, stinky swamps, grand kingdoms of men and deep into the land of the enemy.
        </p>
