    <h1>The Mistborn Trilogy</h1>
    <section id="left">
        <p>
            The Mistborn Trilogy follows the adventures of a young girl named Vin. The setting of this book is a quite
            interesting one. It takes place in a where eons ago, the chosen hero failed. Now the world is no longer the 
            way it is supposed to be. The plants are no longer green, but an ugly brown, the sky is no longer blue but red,
            every night the entire world is covered in a thick mist that makes it hard to see your hand before your eyes, and
            ash frequently rains from the sky. To top this all of the entire world is ruled by an iron-fisted immortal dictator 
            named The Lord Ruler. 
            <br><br>
            The people of the world are divided into two groups, the Nobles and the Skaa. The Nobles are rich and enjoy being on
            the good side of The Lord Ruler. Whereas the Skaa are poor worker people, oppressed and often held as slaves by the 
            Nobility.
            <br><br>
            Vin is an orphaned Skaa. At a young age her mother murdered her younger sister. In response to this her older brother
            took Vin and fled away from their mother. When the story starts Vin is recruited by Kelsier into his thieving gang, and 
            Vin is revealed to be a Mistborn like Kelsier, a person that can ingest metals and perform magical feats with them.
            <br><br>
            Kelsier starts training Vin in how to get better at her new-found powers, meanwhile Kelsier explains the next target for
            his thieving gang. They are going to rob The Lord Ruler himself.
            <br><br>
            This is the plot of the first book in the trilogy, and after this book the scope and the scales only increase. This entire 
            trilogy has a sequal set many years later, called the Wax and Wayne series.
        </p>
