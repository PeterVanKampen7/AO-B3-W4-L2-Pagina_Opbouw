    <h1>Elantris</h1>
    <section id="left">
        <p>
            Elantris is a book I only just recently finished and it is the first book that I did not actually read, but listened to as
            an audio book. This book is again written by Brandon Sanderson, just like Mistborn and The Stormlight Archive. Brandon Sanderson 
            is quickly becoming my favourite author of recent times, and I look forward to reading all his other books.
            <br><br>
            The premise of Elantris is simple, for years there was magic, and then suddenly ten years it stopped working, cursing all those 
            capable of using it. It is called the Shaod, and it has always picked people at random, selecting them to become Elantrians. It used
            to be a blessing now it is a curse. Where before the Shaod would turn people into near demi-gods, now it turns them into monsters. 
            Elantrians now are living corpses, their heart doesnt beat, their blood doesnt flow and their wounds and pains never heal. They dont
            need food, but they hunger still. Elantris used to be a grand city inhabited by magic users, now it is in disarray and the home of 
            the cursed.
            <br><br>
            The book starts with Raoden, he is the crownprince of the kingdom of Arelon, the kingdom that rose from the ashes of Elantris.
            Raoden is taken by the Shaod and thrown into Elantris. Here he must face a complete new set of challanges. Raoden decides that he 
            will not despair and he starts building a primitive society of sorts in Elantris, giving downtrodden people new hope. But outside 
            influences will force Raoden to try and restore Elantris.
            <br><br>
            Sarene is a princess of Teod, travelling to the Kingdom of Arelon to marry Raoden. However when she arrives she learns that he had died.
            Now she must do political battle with the king of Arelon and a foreign priest named Hrethen.
            <br><br>
            Hrethen is a stone-hearted priest from Fjorden, who had been given three months to convert the Kingdom of Arelon to his faith, otherwise
            the people of Arelon will be massacred. He pitches his wit against Sarene, who tries to oppose him.
            <br><br>
            In the end however, it appears that the greatest threat is one they have all been blind to.
        </p>    
