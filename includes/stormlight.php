    <h1>The Stormlight Archive</h1>
    <section id="left">
        <p>
            I should start of by saying that this a series that I am currently in the process of reading, in addition to 
            that, this series is also far from being completed. There are currently four books in this series, but the author
            plans to release 10 books in this series.
            <br><br>
            This series has three primary view characters, Kaladin, Shallan and Dalinar. 
            <br><br>
            Kaladin's story starts of with him being captured and sold into slavery after a battle. While he is a slave he ends up in 
            a bridge crew out on the Shattered Plains, where a big conflict is giong on. As the name suggests, the Shattered Plains contains
            a lot of cracks in the land, cracks that require a bridge to cross. As part of of bridgecrew Kaladin is tasked with carrying the heavy
            wooden bridge across the Plains. It gets really tough when the bridgecrews have to carry the bridge towards a crack where on the other 
            side the enemy is waiting with bows and arrows. Bridgemen die soon. But Kaladin remains among the living, while around him strange
            things start happening.
            <br><br>
            Shallan is a young girl, who comes to a big city apply to be the apprentice of a well known scholar. Through various cunning ways she
            manages to get herself accepted as apprentice. However, Shallan has not come to learn, she came to steal a magical focus from her mentor,
            a fabrial. 
            <br><br>
            Dalinar is a older man who used to be a soldier. He is the brother of a murdered king, and is currently campaigning to avenge the death
            of his brother Gavilar. This campaign brings him to the Shattered Plains, the same location that Kaladin is. Here he struggles with his own
            sense of honor and duty, and starts questioning his own sanity when he starts having visions.
        </p>
